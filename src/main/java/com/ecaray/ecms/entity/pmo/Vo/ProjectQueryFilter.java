public class ProjectQueryFilter {
    @Value("${query.pageNum}")
    private int pageNum;
    @Value("${query.pageSize}")
    private int pageSize;
    /**市场id*/
    private String marktId;
    /**省id*/
    private String provinceId;
    /**城市id*/
    private String cityId;
    /**区域id*/
    private String areaId;
    /**项目状态*/
    private String proStatus;
    /**人员名称*/
    private String peopleName;
    /**项目名称或者项目编码*/
    private String proMsg;
    /**查询范围*/
    private int scope = 1 ;//1 all  2 我参与的  3 我待办的

    private String curUserId;
    private String todoUserId;

    private String partUserId;

    private String authorization;

    public int getPageNum() {
        return pageNum;
    }

    public void setPageNum(int pageNum) {
        this.pageNum = pageNum;
    }

    public int getPageSize() {
        return pageSize;
    }

    public void setPageSize(int pageSize) {
        this.pageSize = pageSize;
    }

    public String getMarktId() {
        return marktId;
    }

    public void setMarktId(String marktId) {
        this.marktId = marktId;
    }

    public String getProvinceId() {
        return provinceId;
    }

    public void setProvinceId(String provinceId) {
        this.provinceId = provinceId;
    }

    public String getCityId() {
        return cityId;
    }

    public void setCityId(String cityId) {
        this.cityId = cityId;
    }

    public String getAreaId() {
        return areaId;
    }

    public void setAreaId(String areaId) {
        this.areaId = areaId;
    }

    public String getProStatus() {
        return proStatus;
    }

    public void setProStatus(String proStatus) {
        this.proStatus = proStatus;
    }

    public String getPeopleName() {
        return peopleName;
    }

    public void setPeopleName(String peopleName) {
        this.peopleName = peopleName;
    }

    public String getProMsg() {
        return proMsg;
    }

    public void setProMsg(String proMsg) {
        this.proMsg = proMsg;
    }

    public int getScope() {
        return scope;
    }

    public void setScope(int scope) {
        this.scope = scope;
    }

    public String getPartUserId() {
        return partUserId;
    }

    public void setPartUserId(String partUserId) {
        this.partUserId = partUserId;
    }

    public String getTodoUserId() {
        return todoUserId;
    }

    public void setTodoUserId(String todoUserId) {
        this.todoUserId = todoUserId;
    }

    public String getCurUserId() {
        return curUserId;
    }

    public void setCurUserId(String curUserId) {
        this.curUserId = curUserId;
    }

    public String getAuthorization() {
        return authorization;
    }

    public void setAuthorization(String authorization) {
        this.authorization = authorization;
    }
}
