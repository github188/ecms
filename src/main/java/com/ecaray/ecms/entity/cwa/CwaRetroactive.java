package com.ecaray.ecms.entity.cwa;

import com.ecaray.ecms.entity.process.ProcessBase;

public class CwaRetroactive  extends ProcessBase{
    private String date;

    private Long fristTime;

    private Long lastTime;

    private Double worktime;

    private String remark;
    
    private Integer processType = 8;

	public String getDate() {
		return date;
	}

	public void setDate(String date) {
		this.date = date;
	}

	public Long getFristTime() {
		return fristTime;
	}

	public void setFristTime(Long fristTime) {
		this.fristTime = fristTime;
	}

	public Long getLastTime() {
		return lastTime;
	}

	public void setLastTime(Long lastTime) {
		this.lastTime = lastTime;
	}

	public Double getWorktime() {
		return worktime;
	}

	public void setWorktime(Double worktime) {
		this.worktime = worktime;
	}

	public String getRemark() {
		return remark;
	}

	public void setRemark(String remark) {
		this.remark = remark;
	}
	@Override
	public Integer getProcessType() {
		return processType;
	}

	public void setProcessType(Integer processType) {
		this.processType = processType;
	}
}