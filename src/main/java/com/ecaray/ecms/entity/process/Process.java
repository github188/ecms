package com.ecaray.ecms.entity.process;

public interface Process {

	/**
	 * 流程展示的标题
	 */
	String getTitle();

	/**
	 * 流程的类型
	 */
	Integer getProcessType();
	
	/**
	 * 流程发起人
	 */
	String getUserId();

	/**
	 * 用于考勤流程时间验证
	 */
	Long getStartTime();
	
	/**
	 * 用于考勤流程时间验证
	 */
	Long getEndTime();
	
	/**
	 * 用于考勤插入记录
	 */
	Integer getType();
	
	String getId();
	
	void setStatus(Integer status);
}
